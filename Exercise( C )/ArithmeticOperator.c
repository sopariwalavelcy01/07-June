#include<stdio.h>
/*
Arithmetic Operators
+ : Addition
- : Subtraction
* : Multiplication
/ : Division
% : Modulus
*/
int main()
{
    int a=10,b=3;
    float c=10.0,d=3.0;
    printf("a+b=%d\n",a+b);
    printf("a-b=%d\n",a-b);
    printf("a*b=%d\n",a*b);
    printf("a/b=%f\n",c/d);
    printf("a%%b=%d\n",a%b); 
    return 0;
}
